package com.web.entity;

import java.io.Serializable;
import java.util.Date;
import jakarta.persistence.Basic;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Id;
import jakarta.persistence.NamedQueries;
import jakarta.persistence.NamedQuery;
import jakarta.persistence.Table;
import jakarta.persistence.Temporal;
import jakarta.persistence.TemporalType;
import jakarta.validation.constraints.NotNull;
import jakarta.validation.constraints.Size;

/**
 *
 * @author Lucy
 */
@Entity
@Table(name = "partners")
@NamedQueries({
    @NamedQuery(name = "Partners.findAll", query = "SELECT p FROM Partners p"),
    @NamedQuery(name = "Partners.findById", query = "SELECT p FROM Partners p WHERE p.id = :id"),
    @NamedQuery(name = "Partners.findByName", query = "SELECT p FROM Partners p WHERE p.name = :name"),
    @NamedQuery(name = "Partners.findByLogoUrl", query = "SELECT p FROM Partners p WHERE p.logoUrl = :logoUrl"),
    @NamedQuery(name = "Partners.findByWebsiteUrl", query = "SELECT p FROM Partners p WHERE p.websiteUrl = :websiteUrl"),
    @NamedQuery(name = "Partners.findByDisplayOrder", query = "SELECT p FROM Partners p WHERE p.displayOrder = :displayOrder"),
    @NamedQuery(name = "Partners.findByCreatedAt", query = "SELECT p FROM Partners p WHERE p.createdAt = :createdAt"),
    @NamedQuery(name = "Partners.findByUpdatedAt", query = "SELECT p FROM Partners p WHERE p.updatedAt = :updatedAt")})
public class Partners implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 36)
    @Column(name = "id")
    private String id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "name")
    private String name;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 512)
    @Column(name = "logo_url")
    private String logoUrl;
    @Size(max = 512)
    @Column(name = "website_url")
    private String websiteUrl;
    @Column(name = "display_order")
    private Integer displayOrder;
    @Basic(optional = false)
    @NotNull
    @Column(name = "created_at")
    @Temporal(TemporalType.TIMESTAMP)
    private Date createdAt;
    @Basic(optional = false)
    @NotNull
    @Column(name = "updated_at")
    @Temporal(TemporalType.TIMESTAMP)
    private Date updatedAt;

    @jakarta.persistence.Transient
    private Integer tempId;
    @jakarta.persistence.Transient
    private Boolean validCell = false;

    public Integer getTempId() {
        return tempId;
    }

    public void setTempId(Integer tempId) {
        this.tempId = tempId;
    }

    public Boolean getValidCell() {
        return validCell;
    }

    public void setValidCell(Boolean validCell) {
        this.validCell = validCell;
    }

    public Partners() {
    }

    public Partners(String id) {
        this.id = id;
    }

    public Partners(String id, String name, String logoUrl, Date createdAt, Date updatedAt) {
        this.id = id;
        this.name = name;
        this.logoUrl = logoUrl;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogoUrl() {
        return logoUrl;
    }

    public void setLogoUrl(String logoUrl) {
        this.logoUrl = logoUrl;
    }

    public String getWebsiteUrl() {
        return websiteUrl;
    }

    public void setWebsiteUrl(String websiteUrl) {
        this.websiteUrl = websiteUrl;
    }

    public Integer getDisplayOrder() {
        return displayOrder;
    }

    public void setDisplayOrder(Integer displayOrder) {
        this.displayOrder = displayOrder;
    }

    public Date getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(Date createdAt) {
        this.createdAt = createdAt;
    }

    public Date getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(Date updatedAt) {
        this.updatedAt = updatedAt;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Partners)) {
            return false;
        }
        Partners other = (Partners) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.web.entity.Partners[ id=" + id + " ]";
    }
    
}
